public class ArithmaticOperators {
    public static void main(String[] args) {
        int x =40;
        int y=4;
        float c =2.2f;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(c/2);
    }
}
